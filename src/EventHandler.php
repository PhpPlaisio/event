<?php

namespace SetBased\Abc\Event;

/**
 * Interface for event handlers (a.k.a. listeners).
 */
interface EventHandler
{
  //--------------------------------------------------------------------------------------------------------------------
  /**
   * Handles an event.
   *
   * @param Event $event The event.
   *
   * @return void
   */
  public function handle(Event $event): void;

  //--------------------------------------------------------------------------------------------------------------------
}

